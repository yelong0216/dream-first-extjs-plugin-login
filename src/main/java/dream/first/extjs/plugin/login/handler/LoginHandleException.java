package dream.first.extjs.plugin.login.handler;

/**
 * 
 * 登录处理异常
 * 
 * @since 2.0
 */
public class LoginHandleException extends Exception {

	private static final long serialVersionUID = 6757054692603536396L;

	public LoginHandleException() {
		super();
	}

	public LoginHandleException(String message, Throwable cause) {
		super(message, cause);
	}

	public LoginHandleException(String message) {
		super(message);
	}

	public LoginHandleException(Throwable cause) {
		super(cause);
	}

}
